package vectores;

/**
 *
 * @author franco
 */
public class invertirVector {
    public static void main(String[] args) {
        
        int [] v={1,3,5,6};
        for (int i = v.length-1; i >= 0; i--) {
            System.out.println(v[i]);
        }
        
    }
}
